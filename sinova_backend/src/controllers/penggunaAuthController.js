import bcrypt from "bcryptjs";
import pool from "../config/db.js";
import jwt from "jsonwebtoken";
import { findPenggunaByEmail, createPengguna } from "../models/penggunaModel.js";

export const registerPengguna = async (req, res) => {
    try {
        const { name, email, password } = req.body;

        const hashed = await bcrypt.hash(password, 10);
        const result = await createPengguna(name, email, hashed);

        res.json({
            message: "Register pengguna berhasil",
            user: result.rows[0]
        });

    } catch (err) {
        console.error(err);
        res.status(500).json({ error: "Server error" });
    }
};
export const getPenggunaProfile = async (req, res) => {
  try {
    const userId = req.user.id;
    const result = await pool.query(
      "SELECT id, name, email, foto_profil FROM users WHERE id = $1",
      [userId]
    );

    if (!result.rows.length)
      return res.status(404).json({ message: "User tidak ditemukan" });

    res.json(result.rows[0]);
  } catch (err) {
    console.error(err);
    res.status(500).json({ message: "Server error" });
  }
};


export const loginPengguna = async (req, res) => {
    try {
        const { email, password } = req.body;

        const result = await findPenggunaByEmail(email);

        if (!result.rows.length) {
            return res.status(400).json({ error: "User tidak ditemukan" });
        }

        const user = result.rows[0];

        const match = await bcrypt.compare(password, user.password);
        if (!match) {
            return res.status(400).json({ error: "Password salah" });
        }

        const token = jwt.sign(
            { id: user.id, name: user.name, role: user.role },
            process.env.JWT_SECRET,
            { expiresIn: "7d" }
        );

        res.json({
    message: "Login berhasil",
    user: {
        id: user.id,
        name: user.name,
        email: user.email,
        role: user.role
    },
    token
});


    } catch (err) {
        console.error(err);
        res.status(500).json({ error: "Server error" });
    }
};
